					<h2 class="h2 pt-10 pb-5">Learning Hiragana and Katakana</h2>
					<p>
						The first step in learning Japanese is to learn the syllabic writing system that
						Japanese uses. Luckily, Tofugu has good guides for both <a
							href="https://www.tofugu.com/japanese/learn-hiragana/"
							class="text-blue-500 hover:underline">hiragana</a
						>
						and
						<a href="https://www.tofugu.com/japanese/learn-katakana" class="anchor">katakana</a>.
					</p>
					<p>Learning Hiragana can be done with a few days of dedication.</p>
					<h2 class="h2 pt-10 pb-5">Learning Kanji</h2>
					<p>
						Once hiragana can be read properly, kanji and grammar can be learned. A few options
						present themselves:
					</p>
					<ul class="list-disc pl-5">
						<li>
							<a href="https://wanikani.com/" class="anchor">Wanikani</a>: Wanikani is a kanji
							learning website created by Tofugu. It uses spaced based repetition for helping
							learning.
						</li>
						<li>
							<a href="https://apps.ankiweb.net/" class="anchor">Anki</a>: Anki is a software that
							you can download that is similar to what wanikani does except that it comes in the
							form of an app that you can download. You also have to download a card deck. More
							information can be found in this
							<a
								href="https://www.reddit.com/r/LearnJapanese/comments/3lhb36/recommended_japanese_anki_deck/"
								class="anchor">Reddit Guide</a
							>.
						</li>
					</ul>
					<h2 class="h2 pt-10 pb-5">Grammar guides and textbook</h2>
					<ul class="list-disc pl-5">
						<li>
							<a href="http://www.guidetojapanese.org/learn/" class="anchor"
								>Tae Kim's Guide to Learning Japanese</a
							>: Taekim's guide is probably the best written guide that can be found for free on
							internet. It has a lot of information and even contains some videos that go in depth
							on some topics. It is highly recommended for those who want to study by themselves and
							don't like the style of GENKI or don't want to buy it.
						</li>
						<li>
							<a href="https://bunpro.jp/summary" class="anchor">Bunpro</a>: Bunpro is the wanikani
							of grammar. It aims to gamify grammar learning and to take full advantage of spaced
							based repetition. It comes with a subscription, but the quality of the content is
							pretty good.
						</li>
						<li>
							<a href="https://www.youtube.com/channel/UCBSyd8tXJoEJKIXfrwkPdbA" class="anchor"
								>Misa from Japanese Ammo</a
							>: Misa makes a lot of videos about Japanese learning. Her guides on how to sound
							native are especially useful to people who study by themselves. By listening to her,
							you can stop saying わたしのにほんごはわるいです and say things like
							にほんごがへたです instead! She also does listening practice.
						</li>
						<li>
							<a
								href="https://www.amazon.co.jp/-/en/%E5%9D%82%E9%87%8E-%E6%B0%B8%E7%90%86/dp/4789017303/ref=sr_1_1?dchild=1&keywords=genki+1&qid=1629658542&sr=8-1"
								class="anchor"
								>GENKI: An Integrated Course in Elementary Japanese I [Third Edition]</a
							>: Genki is considered by many as the best Japanese learning textbook for English
							speakers. It is used in many universities worldwide. Because it is a textbook aimed at
							school classes, it can feel a little dry and is often oriented towards team practice,
							but it is still very good for anyone wanting to learn Japanese.
						</li>
					</ul>
					<h2 class="h2 pt-10 pb-5">Practice</h2>
					<p>
						I believe that reading is very important in practicing Japanese especially when it comes
						to getting fluent with kanji. For this purpose, I think that the browser extension by <a
							href="https://www.japanese.io"
							class="anchor">japanese.io</a
						> can be extremely helpful in reading and learning kanji and vocabulary.
					</p>
					<p>
						Immersion is considered the best way for learning a foreign language. Learning Japanese
						outside of Japan poses an interesting dilemma as it can feel difficult to meet native
						Japanese speakers outside of Japan. Thanks to the internet it is possible to "simulate"
						immersion. For example, one could play games like Final Fantasy XIV which allows for
						players of the global version to play on Japanese servers. By doing so and trying to
						meet new people and making friends, a simulated state of language immersion becomes
						possible.
					</p>
